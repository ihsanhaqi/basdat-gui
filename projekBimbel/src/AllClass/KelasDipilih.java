public class KelasDipilih {
    private String idPelajar;
    private String idKelas;

    public KelasDipilih(String idPelajar, String idKelas) {
        this.idPelajar = idPelajar;
        this.idKelas = idKelas;
    }

    public String getIdPelajar() {
        return idPelajar;
    }

    public String getIdKelas() {
        return idKelas;
    }
}
